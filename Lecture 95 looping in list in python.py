# for loop in List

fruits=['banana','apple','mango','kiwi','pear']
for fruit in fruits:
    print(fruit)      #this will give output in different line 


#While loop in list

fruits1=['banana','apple','mango','kiwi','pear']
i=0
while i<len(fruits1):
    print(fruits1[i])
    i+=1         #this will also print the value in different line

#we can use both for loop and while loop for looping through list in python
 


